import java.util.Scanner;
public class java8393 {
    public static void main(String[] args) {
        Scanner k= new Scanner(System.in);
        int n = k.nextInt();
        int sum= 0;
        for (int i = 0; i <= n ; i++) {
            sum += i;

        }
        System.out.println(sum);







    }

}
