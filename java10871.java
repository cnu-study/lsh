
import java.util.Scanner; //
public class java10871 {
    public static void main(String[] args) {
        Scanner k = new Scanner(System.in);
        int A = k.nextInt();
        int B = k.nextInt();
        System.out.printf("%d\n", A + B);
        System.out.printf("%d\n", A - B);
        System.out.printf("%d\n", A * B);
        System.out.printf("%d\n", A / B);
        System.out.printf("%d\n", A % B);


    }
}

